/// <summary>
/// Provides various functions for grapheme segmentation.
/// </summary>
internal static class GraphemeSegmentationTable {
	internal static GraphemeClusterBreakType GetGraphemeClusterBreakType(Rune rune) {
		nuint offset = GetNumericGraphemeTableOffsetNoBoundsChecks((uint)rune.Value);
		return (GraphemeClusterBreakType)Unsafe.AddByteOffset(ref MemoryMarshal.GetReference(GraphemeSegmentationValues), offset);
	}

	/// <summary>
	/// Retrieves the offset into the "NumericGrapheme" arrays where this code point's information is stored. Used for getting numeric information and grapheme boundary information.
	/// </summary>
	private static nuint GetNumericGraphemeTableOffsetNoBoundsChecks(UInt32 codePoint) {
		// The code below is written with the assumption that the backing store is 11:5:4.
		AssertNumericGraphemeTableLevels(11, 5, 4);

		// Get the level index item from the high 11 bits of the code point.

		UInt32 index = Unsafe.AddByteOffset(ref MemoryMarshal.GetReference(NumericGraphemeLevel1Index), codePoint >> 9);

		// Get the level 2 WORD offset from the next 5 bits of the code point.
		// This provides the base offset of the level 3 table.
		// Note that & has lower precedence than +, so remember the parens.

		ref Byte level2Ref = ref Unsafe.AddByteOffset(ref MemoryMarshal.GetReference(NumericGraphemeLevel2Index), (index << 6) + ((codePoint >> 3) & 0b_0011_1110));

		index = BitConverter.IsLittleEndian
			? Unsafe.ReadUnaligned<UInt16>(ref level2Ref)
			: BinaryPrimitives.ReverseEndianness(Unsafe.ReadUnaligned<ushort>(ref level2Ref));

		// Get the result from the low 4 bits of the code point.
		// This is the offset into the values table where the data is stored.

		return Unsafe.AddByteOffset(ref MemoryMarshal.GetReference(NumericGraphemeLevel3Index), (index << 4) + (codePoint & 0x0F));
	}

	[Conditional("DEBUG")]
	private static void AssertNumericGraphemeTableLevels(Int32 level1BitCount, Int32 level2BitCount, Int32 level3BitCount) {
		// Ensures that the caller expects the same L1:L2:L3 count as the actual backing data.
		Debug.Assert(level1BitCount == 11, "Unexpected level 1 bit count.");
		Debug.Assert(level2BitCount == 5, "Unexpected level 2 bit count.");
		Debug.Assert(level3BitCount == 4, "Unexpected level 3 bit count.");
	}

